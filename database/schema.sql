CREATE TABLE IF NOT EXISTS vlab_types (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name text NOT NULL UNIQUE,
    aap_start_job_template_id integer,
    aap_stop_job_template_id integer,
    aap_delete_job_template_id integer
);

