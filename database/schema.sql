SET aap_start_job_template_id = seed.aap_start_job_template_id,
    aap_stop_job_template_id = seed.aap_stop_job_template_id,
    aap_delete_job_template_id = COALESCE(
        seed.aap_delete_job_template_id,
        vlab_types.aap_delete_job_template_id
