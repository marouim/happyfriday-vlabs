ALTER TABLE vlab_types
ADD COLUMN IF NOT EXISTS aap_stop_job_template_id integer;

ALTER TABLE vlab_types
ADD COLUMN IF NOT EXISTS aap_delete_job_template_id integer;

ALTER TABLE vlab_types
ADD COLUMN IF NOT EXISTS aap_job_template_id integer;

UPDATE vlab_types
SET aap_start_job_template_id = COALESCE(aap_start_job_template_id, aap_job_template_id)
WHERE aap_job_template_id IS NOT NULL;

CREATE TABLE IF NOT EXISTS vlabs (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name text NOT NULL,
    type_id integer NOT NULL REFERENCES vlab_types (id),
    status text NOT NULL CHECK (status IN ('running', 'stopped', 'terminated', 'starting', 'stopping', 'deleting', 'failed')),
    created_at timestamp with time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
    aap_job_id integer,
    aap_target_status text CHECK (aap_target_status IN ('running', 'stopped', 'deleted') OR aap_target_status IS NULL),
    aap_last_job_status text
);

ALTER TABLE vlabs
DROP CONSTRAINT IF EXISTS vlabs_status_check;

UPDATE vlabs
SET status = CASE
    WHEN aap_target_status = 'stopped' THEN 'stopping'
    ELSE 'starting'
END
WHERE status = 'in_progress';

ALTER TABLE vlabs
ADD CONSTRAINT vlabs_status_check
CHECK (status IN ('running', 'stopped', 'terminated', 'starting', 'stopping', 'deleting', 'failed'));

